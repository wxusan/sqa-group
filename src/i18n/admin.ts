export function isAdminLocale(value: unknown): value is AdminLocale {
  return value === "uz" || value === "ru" || value === "en";
}

export function getAdminLocale(params?: { lang?: string | string[] | null }): AdminLocale {
  const lang = Array.isArray(params?.lang) ? params?.lang[0] : params?.lang;
  return isAdminLocale(lang) ? lang : "uz";
}

export function adminHref(
  pathname: string,
  locale: AdminLocale,
  params: Record<string, string | number | undefined | null> = {}
) {
  const search = new URLSearchParams();
  for (const [key, value] of Object.entries(params)) {
    if (value !== undefined && value !== null && value !== "") search.set(key, String(value));
  }
  if (locale !== "uz") search.set("lang", locale);
  const query = search.toString();
  return query ? `${pathname}?${query}` : pathname;
}
